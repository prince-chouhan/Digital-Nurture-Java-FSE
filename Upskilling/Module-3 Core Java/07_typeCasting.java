public class typeCasting {
    public static void main(String[] args){
        double d = 3.14;
        int i = (int) d; 
        System.out.println("Double type Casted to integer: " + i);
        int i2 = 101;
        double d2 = (double) i2;
        System.out.println("Integer type Casted to double: " + d2);
    }
}
